places = [
  "San Francisco",
  "Tokio",
  "Cuba",
  "Hong Kong",
  "Paises Bajos"
]

print("places", places)
print("sorted places", sorted(places))
print("original places", places)
places.reverse()
print("reversed places", places)
places.reverse()
print("reversed again places", places)
places.sort()
print("sort places", places)
places.sort(reverse=True)
print("sort again places", places)

